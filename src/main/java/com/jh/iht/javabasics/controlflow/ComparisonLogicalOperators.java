package com.jh.iht.javabasics.controlflow;

public class ComparisonLogicalOperators {

    // Comparison Operators: Compare two numbers
    public static boolean compareNumbers(int a, int b) {
        return a == b; // Returns true if a is equal to b
    }

    // Logical Operators: Check if both numbers are positive
    public static boolean areBothPositive(int a, int b) {
        return a > 0 && b > 0; // Returns true if both a and b are positive
    }
}
